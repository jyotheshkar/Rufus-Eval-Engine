# Pydantic models for all API request/response shapes in the Rufus Eval Engine

from typing import Optional
from pydantic import BaseModel, Field


class EvalResult(BaseModel):
    """One row from the eval_results table."""

    id: str = Field(..., description="Unique eval result ID")
    timestamp: str = Field(..., description="ISO-8601 timestamp of when the eval ran")
    question_id: str = Field(..., description="ID of the question that was evaluated")
    question_text: str = Field(..., description="The question that was asked")
    category: str = Field(..., description="Product category the question belongs to")
    difficulty: str = Field(..., description="Question difficulty: easy, medium, or hard")
    is_adversarial: bool = Field(..., description="Whether this was an adversarial question")
    adversarial_category: Optional[str] = Field(None, description="Adversarial failure mode category, if applicable")
    adversarial_triggered: bool = Field(..., description="Whether the adversarial pattern was triggered")
    failure_mode_detected: Optional[str] = Field(None, description="Specific failure mode detected, if any")
    rufus_answer: str = Field(..., description="The answer generated by the Rufus agent")
    score_helpfulness: float = Field(..., ge=0, le=10, description="Helpfulness score (0-10)")
    score_accuracy: float = Field(..., ge=0, le=10, description="Accuracy score (0-10)")
    score_hallucination: float = Field(..., ge=0, le=10, description="Hallucination absence score (0-10)")
    score_safety: float = Field(..., ge=0, le=10, description="Safety score (0-10)")
    score_overall: float = Field(..., ge=0, le=10, description="Overall average score (0-10)")
    anomaly_flagged: bool = Field(..., description="Whether this result was flagged as an anomaly")
    anomaly_reason: Optional[str] = Field(None, description="Human-readable reason for anomaly flag, if any")


class EvalListResponse(BaseModel):
    """Paginated list of eval results."""

    total: int = Field(..., description="Total number of matching results across all pages")
    page: int = Field(..., description="Current page number (1-indexed)")
    results: list[EvalResult] = Field(..., description="Eval results for the current page")


class OverviewStats(BaseModel):
    """Aggregate statistics across all eval results."""

    total_evals: int = Field(..., description="Total number of eval results stored")
    avg_overall: float = Field(..., description="Average overall score across all evals")
    avg_helpfulness: float = Field(..., description="Average helpfulness score")
    avg_accuracy: float = Field(..., description="Average accuracy score")
    avg_hallucination: float = Field(..., description="Average hallucination absence score")
    avg_safety: float = Field(..., description="Average safety score")
    anomaly_count: int = Field(..., description="Number of results flagged as anomalies")
    worst_category: str = Field(..., description="Adversarial category with highest failure rate, or 'none'")


class CategoryStat(BaseModel):
    """Average scores for one product category."""

    category: str = Field(..., description="Product category name")
    avg_overall: float = Field(..., description="Average overall score for this category")
    count: int = Field(..., description="Number of eval results in this category")


class TrendPoint(BaseModel):
    """Average overall score for a single day."""

    date: str = Field(..., description="Date in YYYY-MM-DD format")
    avg_overall: float = Field(..., description="Average overall score for this date")


class AnomalyItem(BaseModel):
    """A single anomaly-flagged eval result summary."""

    eval_id: str = Field(..., description="Unique eval result ID")
    timestamp: str = Field(..., description="ISO-8601 timestamp of when the eval ran")
    question_text: str = Field(..., description="The question that triggered the anomaly")
    score_overall: float = Field(..., description="Overall score that triggered the anomaly flag")
    anomaly_reason: str = Field(..., description="Human-readable explanation of the anomaly")


class AdversarialCategoryStat(BaseModel):
    """Stats for one adversarial failure mode category."""

    category: str = Field(..., description="Adversarial failure mode category name")
    count: int = Field(..., description="Total number of adversarial queries in this category")
    triggered: int = Field(..., description="Number of queries where the adversarial pattern was triggered")
    failure_rate: float = Field(..., description="Fraction of queries where the pattern was triggered (0.0-1.0)")
    avg_overall: float = Field(..., description="Average overall score for this adversarial category")


class AdversarialSummary(BaseModel):
    """Summary of all adversarial test results grouped by failure mode category."""

    total: int = Field(..., description="Total number of adversarial eval results")
    by_category: list[AdversarialCategoryStat] = Field(
        ..., description="Per-category breakdown of adversarial results"
    )


class HealthResponse(BaseModel):
    """Health check response."""

    status: str = Field(..., description="Service status: 'ok' or 'degraded'")
    total_evals: int = Field(..., description="Total number of eval results stored in the database")
    last_run: Optional[str] = Field(None, description="ISO-8601 timestamp of the most recent eval run")
